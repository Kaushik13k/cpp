#include <iostream>

int main() {
    int a = 5;

    // Post-Increment / Decrement
    std::cout << a++ << std::endl;

    // Pre-Increment / Decrement
    std::cout << ++a << std::endl;
    
    // &&, ||, !

    // =, +=, -=, *=. /=, %=
    a = a + 5;
    a += 5;

    // &, |, ~, <<, >>, ^
    
    return 0;
}